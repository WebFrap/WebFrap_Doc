
<h1>Receiver  Variablen</h1>

<p>Beinhaltet Informationen über den Empfänger der Message</p>

<label>Liste der Receiver Variablen</label>
<ul>
  <li><span class="value" >{@receiver:firstname@}</span> 
    Vorname
  </li>
  <li><span class="value" >{@receiver:lastname@}</span> 
    Nachname
  </li>
  <li><span class="value" >{@receiver:title@}</span> 
    Titel
  </li>
  <li><span class="value" >{@receiver:email@}</span> 
    E-Mail Addresse
  </li>
</ul>