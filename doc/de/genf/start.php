<h1>GenF, Das Generator & Compiler Framework</h1>

<p>
Das GenF Projekt bildet die Basis für den Codegenerator.
Es besteht aus mehreren Softwarepacketen.
Zuerst wäre GenF, das Basis Projekt welches den einfachen
Zugriff auf die Modellknoten ermöglicht, und alle Basisklassen der Templates enthält.
Darüber hinaus enthält GenF wesentliche Teile des 5GL Interpreters welcher die Semantischen
Information im Modell verarbeitet.<br />
WebFrap_Genf_Cartridge_* sind die Cartridge Projekte. Sie enthalten die Code Templates, welche
das Domainmodell (4GL) zu Code transformieren.<br />
WebFrap_Lib_BDL enthält den BDL Compiler welcher die 3GL Componenten des Modells in Code compiliert.
</p>

