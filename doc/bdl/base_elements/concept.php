<h2>Concepts</h2>

<p>
Concepts kann man am Besten als "Modell Annotationen" beschreiben.
Im Großen und Ganzen werden sie wie Annotationen eingesetzt, können jedoch
mehr Einfluss auf das Modell nehmen wie das mit einfachen Annotationen (Annotation aus akademischer Sicht) 
möglich wäre.
</p>

<p>
  Die einfachste Form von Concepts ist das simple Mappen von Eigenschaften auf
  den "annotierten" Vaterknoten. Komplexe Concepts können implizit umfassende
  Erweiterungen des Modells durch den 5GL Interpreter nach sich ziehen.
</p>