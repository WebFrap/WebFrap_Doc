<h2>BDL (Business Description Language)</h2>

<p>
BDL ist eine XML basierte Modell-Sprache.
Der Fokus liegt auf Business Anwendungen der effizienten Modellierung von komplexen
Business Strukturen, Prozessen und Dokumenten.
</p>

<p>
BDL ist keine reine Modellsprache, sie umfasst und vereinigt im Gegensatz zu herkömmlichen Sprachen
3 verschiedene Abstraktionsschichten.<br />
Die oberste und zugleich abstrakteste Schicht ist ein Semantiklayer. 
Schicht 2 ist ein Modellayer, die Dritte Schicht ist ein reiner Logiklayer, welcher
auf der Abstraktionsebene herkömmlicher Hochsprachen operiert.
</p>

<p>Diese Aufteilung ermöglicht es die anfallenden Aufgaben und Probleme in der
jeweils optimalen Abstraktion zu lösen.</p>

<p>
Der Aufwand zur Erstellung einer Applikation wird so auf fast auf das Niveau
eines zu 100% eindeutig/maschineninterpretierbaren Anforderungsdokuments reduziert.
Anders formuliert heißt dass, GenF benötigt von BDL nur die expliziten Informationen
welche auch ein Menschlicher Entwickler benötigen würde um das Projekt zu implementieren, 
allerdings in einer formalisierten eindeutigen Notation. (Unter der Vorraussetzung, dass die
aktuelle Architektur den Anwendunsfall bereits abdeckt.)
</p>

