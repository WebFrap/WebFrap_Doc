<h1>Abstraktionsvermögen</h1>

<p>
Gerade das Arbeiten mit WebFrap erfordert ein hohes Maß an korrekter Abstraktion.
Der Entwickler steht vor dem Problem die praktisch unedlich komplexe Realität mit
einer endlichen (aber erweiterbaren) Menge von Elementen abbilden zu müssen.
</p>

<p>
Richtige Abstraktion ist, wenn das Prinzip verstanden wurde jedoch relativ einfach.
</p>

potentielle fehler:

- falsche bezeichner  zu hoch abstrakt, oder zu speziell für den anwendungsfall
- namens kollisionen
- redundante datenknoten
- zu große datenknoten ( duzende attribute )
- spezielle eigenschaften in allgemeinen datenknoten
