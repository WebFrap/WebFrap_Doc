<h2>Entity Concepts</h2>

<p>
  Concepts sind wie bereits erwähnt Modellelemente mit denen in diesem 
  Fall die Entity mit zusätzlichen Informationen und Eigenschaften annotiert werden
  können.<br />
  Wichtig ist jedoch zu beachten, dass nur eine begrenzte Menge von Concepts
  kompatible zu Entity Nodes sind, da bestimmte Konzepte direkt auf der Entity
  einfach keinen Sinn ergeben.
</p>

<p>
Mehr Informationen zu den jeweiligen Concepts sind den passenden Unterpunkten 
zu entnehmen.
</p>

