<h2>Zielgruppe & Zielprojekte</h2>

<p>
BDL ist so aufgebaut um sehr komplexe Business Systeme effizient beschreiben
zu können.
Damit zielt BDL auf alle Anwender und Betreiber von Business Systemen
die eigene Komplexe Prozesse in ihren IT-Systemen abbilden müssen, jedoch
die Qualität einer ausgereiften Stanard Software benötigen.
</p>

<p>
Da diese Systemen Technisch bzw. auf der Architekturebene praktisch identisch
aufgebaut werden können mit dem Sprachunfang von BDL nahezu alle BusinessSystem Arten
abgebildet werden.<br />
</p>

