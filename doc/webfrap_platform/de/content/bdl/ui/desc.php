<h1>UI</h1>

<p>
Mit den UI Nodes kann das UI des aktuellen Viewports sowie die Navigartion von Masken nahezu komplett gecustomized werden.<br />
Die Hauptlayout Elemente wie Maintabs und Modals geben zwar einen Rahmen vor, innerhalb davon kann jedoch komplett frei gestaltet werden.
</p>

<p>
Im interesse der Anwender sollte jedoch davon abgesehen werden zu sehr vom Standard und damit dem bekannten Look & Feel abzuweichen.
</p>

