
<h1>Items</h1>

<p>
Items sind spezialisierte UI Elemente. Sie ermöglichen es einfach optimierte
Visualisierungs und Datenverwaltende Elemente zu Erstellen und zu Integrieren.
</p>

<p>Ein Item muss jedoch kein komplexes UI Element sein, es kann auch eine simple
Tabel, Treetabel, etc. sein. Relevant ist dass es überall in (natürlich passender) Relation eingebunden 
werden kann, auch ohne das Referenzen deklariert sein müssen.
</p>