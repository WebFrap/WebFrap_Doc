
<h1>Environment Variablen</h1>

<p>Über die Env Variable können verschiedenen System Informationen in die
Nachricht eingebunden werden.</p>

<label>Liste der Env Variablen</label>
<ul>
  <li><span class="value" >{@env:server_address@}</span> 
    Die Serveradresse um korrekte links auf das System einbinden zu können</li>
  <li><span class="value" >{@env:system_name@}</span> 
    Der Name des Systems</li>
  <li><span class="value" >{@env:gw_name@}</span> 
    Der Name des Gateways</li>
  <li><span class="value" >{@env:app_name@}</span> 
    Der offizielle Name der Applikation, also der Name unter dem die User 
    idealerweise das System kennen</li>
</ul>
